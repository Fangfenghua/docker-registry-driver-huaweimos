#===============================================================================
# 前缀（prefix）与分隔符（delimiter）第一次出现之间的字符串将保存到CommonPrefix列表中
#===============================================================================
class CommonPrefix(object):
    
    #===========================================================================
    # 初始化  生成前缀
    #===========================================================================
    def __init__(self, prefix):
        self.prefix = prefix







